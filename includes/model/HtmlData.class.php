class HtmlData {

    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////

    private static $_title = "";
    private static $_description = "";
    private static $_keywords = "";
    private static $_templateFilePath = "";
    private static $_httpStatus = 200;
    public static $_urlBase;

    public static function PathRoot(){
        return self::$_urlBase;
    }
    public static function setPathRoot($value) {
        self::$_urlBase = $value;
    }
    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////
    private static $_scriptList = Array(
        "assets/js/main.js",
        "assets/js/main.js",
        "assets/js/main.js",
        //"https://www.google.com/recaptcha/api.js"
    );
    private static $_cssList = Array(
        "assets/css/style.css",
    );
    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////
    public static function ScriptList() {
        return self::$_scriptList;
    }
    public static function addScript($path) {
        self::$_scriptList[] = $path;
    }
    public static function CssList() {
        return self::$_cssList;
    }
    public static function addCss($path) {
        self::$_cssList[] = $path;
    }
    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////
    public static function Title() {
        return self::$_title;
    }
    public static function setTitle($value) {
        self::$_title = $value;
    }
    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////
    public static function Description() {
        return self::$_description;
    }
    public static function setDescription($value) {
        self::$_description = $value;
    }
    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////
    public static function Keywords() {
        return self::$_keywords;
    }
    public static function setKeywords($value) {
        self::$_keywords = $value;
    }
    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////
    public static function TemplateFilePath() {
        return self::$_templateFilePath;
    }
    public static function setTemplateFilePath($value) {
        self::$_templateFilePath = $value;
    }
    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////
    public static function forceHttpStatus($value) {
        self::$_httpStatus = $value;
    }

    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////
    public static function currentHttpStatus() {
        return self::$_httpStatus;
    }
    ///////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////

    public static function getHeader() {
        switch(self::$_httpStatus)
        {
            case 200:
                return "Content-Type: text/html; charset=UTF-8";
            case 404:
            default :
                return "HTTP/1.0 404 Not Found";
        }
    }
} 